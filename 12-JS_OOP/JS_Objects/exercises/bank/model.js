
export function Address(id,country,city){
    this.id=id;
    this.country=country;
    this.city=city;
}

export function Client(id,firstName,lastName,dob,email,address){
    this.id=id;
    this.firstName=firstName;
    this.lastName=lastName;
    this.dob=dob;
    this.email=email;
    this.address=address;
}

